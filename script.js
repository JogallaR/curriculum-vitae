function baixarPDF() {
  const wrapper = document.getElementById('cvWrapper');
  const preview = wrapper.parentElement;
  const el = document.getElementById('cv-a4');
  
  // Guardem els estats previs essencials
  const originalPreviewScroll = preview.scrollTop;
  const originalShadow = el.style.boxShadow;
  
  // Resetejem l'entorn visual al complet netejant el layout dinàmic
  wrapper.style.width = '';
  wrapper.style.height = '';
  wrapper.style.position = '';
  el.style.position = '';
  el.style.top = '';
  el.style.left = '';
  el.style.transform = 'scale(1)';
  el.style.boxShadow = 'none';
  
  preview.scrollTop = 0;
  window.scrollTo(0, 0);

  const nom = document.getElementById('inNom').value || 'CV';
  
  // Obtenir data actual en format DD-MM-YYYY
  const avui = new Date();
  const dia = avui.getDate().toString().padStart(2, '0');
  const mes = (avui.getMonth() + 1).toString().padStart(2, '0');
  const any = avui.getFullYear();
  const dataActual = `${dia}-${mes}-${any}`;
  
  setTimeout(() => {
    html2pdf().set({
      margin: 0,
      filename: `${nom}_CV_${dataActual}.pdf`,
      image: { type: 'jpeg', quality: 1 },
      html2canvas: { scale: 3, useCORS: true, scrollY: 0 },
      jsPDF: { unit: 'px', format: [794, 1123], orientation: 'portrait' }
    }).from(el).save().then(() => {
      // Restaurar estats visualització
      el.style.boxShadow = originalShadow;
      scaleCV(); // Refem l'escalat complert exactament
      preview.scrollTop = originalPreviewScroll;
    });
  }, 150);
  
  notify(T[lang]['notif-pdf'], 'ok');
}

