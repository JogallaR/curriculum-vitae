function scaleCV() {
  const wrapper = document.getElementById('cvWrapper');
  const el = document.getElementById('cv-a4');
  if(!wrapper || !el) return;
  const preview = wrapper.parentElement;
  
  const isMobile = window.innerWidth <= 768;
  const padding = isMobile ? 20 : 40;
  const previewW = preview.clientWidth - padding;
  
  const cvW = 794; /* 210mm exacte en resolució 96dpi (CSS pixels) */
  const cvH = 1123;
  const scale = Math.min(1, Math.max(0.1, previewW / cvW));
  
  el.style.position = 'absolute';
  el.style.top = '0';
  el.style.left = '0';
  el.style.transformOrigin = 'top left';
  el.style.transform = `scale(${scale})`;
  
  // El wrapper adquireix els píxels exactes de la pantalla (evita scrollbars falses)
  wrapper.style.position = 'relative';
  wrapper.style.width = `${cvW * scale}px`;
  wrapper.style.height = `${cvH * scale}px`;
  
  // Limpiem estats residuals antic
  wrapper.style.transform = '';
  wrapper.style.marginBottom = '';
}

